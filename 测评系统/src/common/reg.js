export default {
  // 邮箱验证
  email: /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  // 手机验证   规则：以1开头,后面必须是10位数字组成
  mobile: /^1\d{10}$/,
  // 整数或者小数 保留两位有效小数
  number: /^-?[0-9]+\.{0,1}[0-9]{0,2}$/,
  // 整数验证  
  int: /^[1-9]\d*$/,
  //英文字母验证
  alphaRegex: /^[a-z]+$/i,
  //英文加数字
  alphaNumericRegex: /^[a-z0-9]+$/i,
  //url地址验证
  urlRegex: /^((http|https):\/\/(\w+:{0,1}\w*@)?(\S+)|)(:[0-9]+)?(\/|\/([\w#!:.?+=&%@!\-\/]))?$/,
  //ip验证
  ipRegex: /^((25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]{1,2})\.){3}(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]{1,2})$/i,
  //日期格式验证 目前只支持 2018-xx-xx
  date: /^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)-02-29)$/,
  // 金额验证  目前只支持保留两位小数
  money: /^(-)?(([1-9]{1}\d*)|([0]{1}))(\.(\d){1,2})?$/,
  //固定电话验证
  tel: /^\d{3}-\d{7,8}|\d{4}-\d{7,8}$/,
  // 密码验证  规则：8-16位(需至少包含1个数字、1个小写字母、1个大写字母和1个特殊字符)
  pwd: /^.*(?=.{8,16})(?=.*\d)(?=.*[A-Z]{1,})(?=.*[a-z]{1,})(?=.*((?=[\x21-\x7e]+)[^A-Za-z0-9])).*$/
}
